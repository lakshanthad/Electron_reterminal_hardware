// load the led library
const led = require('npm-reterminal/lib/led');

// turn on staGreen
function staGreenOn()
{
    led.staGreenOn();
}

// turn off staGreen
function staGreenOff()
{
    led.staGreenOff();
}

// turn on staRed
function staRedOn()
{
    led.staRedOn();
}

// turn off staRed
function staRedOff()
{
    led.staRedOff();
}

// turn on usrGreen
function usrGreenOn()
{
    led.usrGreenOn();
}

// turn off usrGreen
function usrGreenOff()
{
    led.usrGreenOff();
}

